    public boolean verifyFields(){
        String nickname = nickName.getText();
        String username = userName.getText();
        String password = String.valueOf(passWord.getPassword());
        String conpassword = String.valueOf(conPass.getPassword());
        
        if(nickname.trim().equals("") || nickname.equals("Nickname") || username.trim().equals("") || username.equals("Username") || password.trim().equals("") || password.equals("Password") || conpassword.trim().equals("") || conpassword.equals("Confirm Password")){
            JOptionPane.showMessageDialog(null, "One or more fields are empty", "Please fill all the required data",2);
            return false;
        }
        else if(!password.equals(conpassword)){
            JOptionPane.showMessageDialog(null, "Password doesn't match", "Confirm Password",2);
            return false;
        }
        else{
            return true;
        }
    }
    
