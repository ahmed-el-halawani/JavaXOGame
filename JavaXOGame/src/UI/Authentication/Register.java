    private void registerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerBtnActionPerformed
        String nickname = nickName.getText();
        String username = userName.getText();
        String password = String.valueOf(passWord.getPassword());
        String conpassword = String.valueOf(conPass.getPassword());
        
        if(!verifyFields())
            return;
            if(userCrud.getWithUserName(username) != null){
                JOptionPane.showMessageDialog(null, "This Username is already taken", "Please choose another one", 2);
                return ;
            }
                
                   int i = userCrud.add(new User(nickname, username, password));
                    
                    if(i != 0){
                        JOptionPane.showMessageDialog(null, "Your account has been registered Successfully");
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "Error, please check your information");
                    }
                
            
        
        
    }//GEN-LAST:event_registerBtnActionPerformed

